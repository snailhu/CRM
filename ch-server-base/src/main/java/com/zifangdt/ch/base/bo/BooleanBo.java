public class BooleanBo {
    private boolean value;

    public BooleanBo() {
    }

    public BooleanBo(boolean value) {
        this.value = value;
    }

    public boolean isValue() {
        return value;
    }

    public void setValue(boolean value) {
        this.value = value;
    }
}
